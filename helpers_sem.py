def cut_query(query):
    query_words = query.split()
    if len(query_words) > 100:
        query_words_wo_stop = [w for w in query_words if w not in russian_stopwords]
        query_words_wo_stop = query_words_wo_stop[:100]
        query = ' '.join(query_words_wo_stop)
    return query